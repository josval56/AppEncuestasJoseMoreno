     public void resultados(String NumeroCliente, Double promedio, String Nombre, String Apellido){
        
        lbl_nombre_apellido.setText(Nombre.toUpperCase() + " " + Apellido.toUpperCase());
        lbl_nombre.setText(NumeroCliente);
        lbl_resultado.setText(String.format("%.0f", promedio));
        BuenoRegularMalo(promedio);
      
        
    }
     public void BuenoRegularMalo (Double promedio){
         if(promedio > 5){
             rb_bueno.setSelected(true);
         }if (promedio == 5){
             rb_regular.setSelected(true);
             
         }else
             rb_malo.setSelected(true);
             
     }

